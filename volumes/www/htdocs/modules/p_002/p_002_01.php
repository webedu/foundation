		Beim Vorgang der Hydrolyse werden vor allem <A HREF="javascript:myopen('Carbonate')"><img src="<?php echo $teilpro . "/"; ?>p_002/img/navigation/glossar.gif" BORDER="0">&nbsp;Carbonate</a> 
