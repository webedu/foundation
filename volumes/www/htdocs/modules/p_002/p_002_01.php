		<font face="Arial">
		Die eigentliche Triebkraft dieser Verwitterungsart ist in der Regel die Reaktion eines Minerals mit den Wassermolekülen bzw.
		H+-Ionen der L&ouml;sung, die sich an die Grenzflächenionen von Kristallen anlagern und Kationen aus dem Kristallgitter ersetzten.
		Diesen Vorgang nennt man auch Kationenaustausch. Die Kristallstruktur wird aufgeweitet und zerfällt bis zur kompletten Auflösung
		des Kristallgitters.
		<p><hr color="#D8A147" noshade width="500" size="1"></p>
		Anhand eines Silikates - in diesem Beispiel einem Feldspat - wird der Prozess der Hydrolyse erkl&auml;rt.
		Anschlie&szlig;end werden die diesen Prozess steuernden Paramter pH-Wert, klimatische Bedingungen (Temperatur, Wassergehalt) und die
		Zeit miteinbezogen.
