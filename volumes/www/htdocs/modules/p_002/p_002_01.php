		die den gr&ouml;&szlig;ten Teil der gesteinsbildenden Minerale (zusammen ca. 60-65 Gew.%) ausmachen, zersetzt.
		Es handelt sich hierbei um Verbindungen, die aus einer schwachen S&auml;ure und/oder einer schwachen Base bestehen.
